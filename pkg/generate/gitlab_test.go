func TestGitLab_MultiplePipelines(t *testing.T) {
	cfg := &config.Config{
		Pipelines: map[string]config.Pipeline{
			"pr":   {Phases: []string{"security", "code", "test"}},
			"main": {Phases: []string{"security", "code", "test", "build"}},
		},
	}

	output, err := GitLab(cfg)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if !strings.Contains(output, "CI_MERGE_REQUEST_ID") {
		t.Error("expected merge request rule")
	}
	if !strings.Contains(output, "CI_DEFAULT_BRANCH") {
		t.Error("expected default branch rule")
	}
	if !strings.Contains(output, "./bin/cidx run build") {
		t.Error("expected build job")
	}
}

func TestGitLab_PhasesDeduplication(t *testing.T) {
	cfg := &config.Config{
		Pipelines: map[string]config.Pipeline{
			"pr":   {Phases: []string{"security", "code", "test"}},
			"main": {Phases: []string{"security", "code", "test", "build"}},
		},
	}

	output, err := GitLab(cfg)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	count := strings.Count(output, "./bin/cidx run security")
	if count != 1 {
		t.Errorf("expected security phase once, found %d times", count)
	}
}


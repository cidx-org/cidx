func TestGitLab_BasicCI(t *testing.T) {
	cfg := &config.Config{
		Pipelines: map[string]config.Pipeline{
			"ci": {Phases: []string{"security", "code", "test"}},
		},
	}

	output, err := GitLab(cfg)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	checks := []struct {
		name     string
		expected string
	}{
		{"header", "Generated by cidx"},
		{"stages", "stages:"},
		{"bootstrap stage", "- bootstrap"},
		{"security stage", "- security"},
		{"bootstrap job", "bootstrap:"},
		{"golang image", "golang:1.23-alpine"},
		{"dind service", "docker:dind"},
		{"needs bootstrap", "- bootstrap"},
		{"cidx run security", "./bin/cidx run security"},
		{"cidx run code", "./bin/cidx run code"},
		{"cidx run test", "./bin/cidx run test"},
	}

	for _, check := range checks {
		if !strings.Contains(output, check.expected) {
			t.Errorf("%s: expected %q in output", check.name, check.expected)
		}
	}
}

